export const getEraColor = (era: Era): string => {
  const colors: Record<Era, string> = {
    Ancient: "era-ancient",
    Classical: "era-classical",
    "Post-Classical": "era-postclassical",
    Medieval: "era-medieval",
    Colonial: "era-colonial",
  };
  return colors[era];
};

export const getReligionColor = (religion: Religion): string => {
  const colors: Record<Religion, string> = {
    Hindu: "religion-hindu",
    Buddhist: "religion-buddhist",
    Muslim: "religion-muslim",
    Mixed: "religion-mixed",
    Unknown: "religion-unknown",
    Protestant: "religion-protestant",
  };
  return colors[religion];
};

export const formatYear = (year: number): string => {
  if (year < 0) {
    return `${Math.abs(year)} BCE`;
  }
  return `${year} CE`;
};
